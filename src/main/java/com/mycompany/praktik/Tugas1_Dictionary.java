    private void cekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cekActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelTable = (DefaultTableModel) jTable1.getModel();
        // set row count digunakan agar selalu mulai dari
        modelTable.setRowCount(0);
        for (String[] kamus : kamusList) {
            String[] dataListTemp = {kamus[0], kamus[1]};
            modelTable.addRow(dataListTemp);
        }
    }//GEN-LAST:event_cekActionPerformed

    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
      String kataCari = jTextField1.getText().toUpperCase();
      boolean inputInvalid = kataCari.equals("");

      if (!inputInvalid) {
        String found = (modeTranslate == 0 ? "TIDAK KETEMU!" : "NOT FOUND!");
        for (String[] kamus : kamusList) {
          if (kamus[modeTranslate].equals(kataCari)) {
            found = kamus[modeTranslate == 0 ? 1 : 0];
            break;
          }
        }
          jTextField2.setText(found);
      } else {
        JOptionPane.showMessageDialog(
          rootPane,
          modeTranslate == 0 ? "Masukkan kata yang dicari!" : "Enter the word you are looking for!",
          modeTranslate == 0 ? "Kesalahan" : "Error",
          JOptionPane.ERROR_MESSAGE
        );
      }
    }//GEN-LAST:event_searchActionPerformed

