    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
      // String kataIdn; = (mode == 0 ? inputKata1.getText().toUpperCase()  : inputKata2.getText().toUpperCase());
      // String kataEng; = (mode == 0 ? inputKata2.getText().toUpperCase() : inputKata1.getText().toUpperCase());
      String kataIndo;
      String kataIngg;
      if (modeTranslate == 0) {
          kataIndo = jTextField1.getText().toUpperCase();
          kataIngg = jTextField2.getText().toUpperCase();
          
      } else {
          kataIndo = jTextField2.getText().toUpperCase();
          kataIngg = jTextField1.getText().toUpperCase();
      }

      boolean ketemu = false;
      boolean invalid = false;
      for (String[] kamus : kamusList) {
        if (kamus[0].equals(kataIndo) || kamus[1].equals(kataIngg)) {
          ketemu = true;
          // kondisi dibawah adalah ternary if dimana menjelaskan jika modeTranslate == 0 maka akan mencari di kata indo else kata inggris
          invalid = kamus[modeTranslate].equals(modeTranslate == 0 ? kataIndo : kataIngg);
          break;
        }
      }
      if(!ketemu){
        String[] dataListTemp = {kataIndo, kataIngg};
        kamusList.add(dataListTemp);

        DefaultTableModel modelTable = (DefaultTableModel) jTable1.getModel();
        modelTable.addRow(dataListTemp);
        if(modeTranslate==0){
            JOptionPane.showMessageDialog(rootPane, "Kata-kata berhasil ditambahkan ke kamus");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Input success!");
        }
      }else{
          if(modeTranslate==0){
              JOptionPane.showMessageDialog(rootPane, "Kata-kata sudah ada di kamus");
          } else{
              JOptionPane.showMessageDialog(rootPane, "This word has been added before");
          }
      }
    }//GEN-LAST:event_simpanActionPerformed

